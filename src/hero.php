<script>
    $(document).ready(function() {
    var tabs = $('.tabs-item');
    var currentIndex = 0;
    var intervalTime = 5000; // Time for each tab in milliseconds
    var autoplay;

    function switchTab(index) {
        var target = $(tabs[index].getAttribute('data-target'));
        var allVideoContainers = $('.video-container');

        allVideoContainers.removeClass('active').addClass('hidden');
        target.removeClass('hidden').addClass('active');

        var videoElement = target.find('video')[0];
        if (videoElement) {
            videoElement.onloadedmetadata = function() {
                videoElement.play();
                videoElement.onplaying = function() {
                    tabs.removeClass('active').find('.progress').css('width', '0%');

                    $(tabs[index]).addClass('active');
                    var progressBar = $(tabs[index]).find('.progress');
                    animateProgressBar(progressBar, intervalTime, function() {
                        currentIndex = index;
                        autoplay = setTimeout(autoplayTabs, intervalTime);
                    });
                };
            };
        }
    }

    function animateProgressBar(element, duration, callback) {
        var start = null;

        function step(timestamp) {
            if (!start) start = timestamp;
            var progress = Math.min((timestamp - start) / duration, 1);
            element.css('width', (progress * 100) + '%');
            // if (progress < 1) {
            //     window.requestAnimationFrame(step);
            // } else {
            //     callback();
            // }
        }
        window.requestAnimationFrame(step);
    }

    function autoplayTabs() {
        clearTimeout(autoplay);
        currentIndex = (currentIndex + 1) % tabs.length;
        switchTab(currentIndex);
    }

    tabs.each(function(index) {
        $(this).click(function() {
            clearTimeout(autoplay);
            switchTab(index);
        });
    });

    switchTab(currentIndex);
});

</script>

